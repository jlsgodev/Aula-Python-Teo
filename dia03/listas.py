

# %%

minha_lista_vazia = []  # cria uma lista vazia
print(minha_lista_vazia) # imprime a lista vazia




# %%

minha_lista = ["jhon", "Goncalves", 32, 1.79]  # cria uma lista com 4 elementos

print(minha_lista) # imprime a lista


# %%


minha_lista[0] # acessa o primeiro elemento da lista

# %%

minha_lista[len(minha_lista) - 1] # acessa o último elemento da lista, (o uso de len() é para pegar o tamanho da lista)


# %%
minha_lista[-1] # acessa o último elemento da lista, (o uso de -1 é para pegar o último elemento da lista)
# %%

minha_lista[-2] # acessa o penúltimo elemento da lista, (o uso de -2 é para pegar o penúltimo elemento da lista)

# %%
minha_lista[1:3] # acessa os elementos da posição 1 até a posição 3 (não inclusa)


# %%
minha_lista[1:] # acessa os elementos da posição 1 até o final da lista

# %%
minha_lista[:3] # acessa os elementos do início da lista até a posição 3 (não inclusa)

# %%
minha_lista[0:-1] # acessa os elementos do início da lista até o penúltimo elemento


# %%



minha_lista[::] # acessa todos os elementos da lista

# %%
minha_lista[::2] # acessa todos os elementos da lista de 2 em 2

# %%

minha_lista[::-1] # acessa todos os elementos da lista de trás para frente

